package org.example.homeTasks.three;

public class Two {
    public static void main(String[] args) {

        // Home task two
        System.out.println("Table of multiplication inside out: ");
        for (int i = 10; i > 0; i--) {
            for (int j = 10; j > 0; j--) {
                System.out.println(i + "*" + j + "=" + i * j);
            }
        }
    }
}
